import { Types } from "mongoose";



export type TUserName = {
  firstName: string;
  middleName: string;
  lastName: string;
};


export type TStudent = {
  studentData: import("mongoose").Schema.Types.ObjectId;
  id: string;
  user : Types.ObjectId;
  password: string;
  name: TUserName;
  gender: 'male' | 'female' | 'other';
  dateOfBirth?: string;
  email: string;
  contactNo: string;
  address: string;
  profileImg?: string;
  isActive: 'active' | 'blocked';
  isDeleted: boolean;
};

